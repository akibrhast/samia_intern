/**
  * This type group together the components of the clock three to be modified during
  * a power mode change.
  */
typedef struct _system_clock_t
{
  uint32_t latency;
  RCC_OscInitTypeDef osc;
  RCC_ClkInitTypeDef clock;
  RCC_PeriphCLKInitTypeDef periph_clock;
} system_clock_t;

/**
  * It is used to save and restore the system clock during the power mode switch.
  */
static system_clock_t sSystemClock;


/* Public functions declaration */
/********************************/

/**
  * Function generated by CubeMX.
  */
void SystemClock_Config(void);

/**
  * Function generated by CubeMX.
  */
void SystemPower_Config(void);

/**
  * Function generated by CubeMX.
  * @brief  Period elapsed callback in non blocking mode
  * @note   This function is called  when TIM6 interrupt took place, inside
  * HAL_TIM_IRQHandler(). It makes a direct call to HAL_IncTick() to increment
  * a global variable "uwTick" used as application time base.
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim);

/**
  * Function generated by CubeMX.
  * This function is executed in case of error occurrence in the HAL layer.
  * It is remapped in the eLooM global sys_error_handler() function.
  * @retval None
  */
void Error_Handler(void);

/**
  * Called by the ::AppPowerModeHelper object during the power mode transaction.
  * Save the actual clock three configuration.
  */
void SystemClock_Backup(void);

/**
  * Called by the ::AppPowerModeHelper object during the power mode transaction
  * before enter a low power mode.
  * Restore original clock parameters
  */
void SystemClock_Restore(void);

